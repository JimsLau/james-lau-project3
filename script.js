const animals = ['Monkey','Rooster','Dog', 'Pig', 'Rat', 'Ox', 'Tiger', 'Rabbit', 'Dragon', 'Snake', 'Horse', 'Sheep'];

function getAnimalName(year) {
	if(year) {
		const animalIndex = year % 12;
		return animals[animalIndex];
	}
	
	return "";
}

function getAnimalInformation(animalName) {
	let index = 0;
	
	while(index < zodiacInfo.length) {
		if(animalName == zodiacInfo[index].name) {
			return zodiacInfo[index];
		}
		
		index ++;
	}
	
	return {}
}

function showZodiacInformation(animalName) {
	const $information = $('.information');
	
	$information.find('.zodiac-animal').text(animalName);
	
	const animalInformation = getAnimalInformation(animalName);
	$information.find('img').attr('src', animalInformation['image']);
	$information.find('.personality').text(animalInformation['description']);
}

